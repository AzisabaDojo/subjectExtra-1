public class Extra {

    public static void main(String[] args) {
        int apple = 500;
        int eatApple = 0;

        System.out.println("リンゴの数は" + apple + "個です。");

        eatApple++;

        System.out.println(eatApple + "個食べていいですよ");

        apple--;

        System.out.println("残りのリンゴの数は" + apple + "個です。");
    }

}
